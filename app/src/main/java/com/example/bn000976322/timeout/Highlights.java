package com.example.bn000976322.timeout;

public class Highlights {

    int highlightsTitle;
    int highlightsThumbnail;
    int highlightsUrl;
    int highlightsDescription;

    public int getHighlightsDescription() {
        return highlightsDescription;
    }

    public void setHighlightsDescription(int highlightsDescription) {
        this.highlightsDescription = highlightsDescription;
    }

    public int getHighlightsTitle() {
        return highlightsTitle;
    }

    public void setHighlightsTitle(int highlightsTitle) {
        this.highlightsTitle = highlightsTitle;
    }

    public int getHighlightsThumbnail() {
        return highlightsThumbnail;
    }

    public void setHighlightsThumbnail(int highlightsThumbnail) {
        this.highlightsThumbnail = highlightsThumbnail;
    }

    public int getHighlightsUrl() {
        return highlightsUrl;
    }

    public void setHighlightsUrl(int highlightsUrl) {
        this.highlightsUrl = highlightsUrl;
    }
}
